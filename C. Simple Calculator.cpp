#include <iostream>
using namespace std ;
int main (){
    long long i, x , sum , mul,sub ;
    cin >> i >> x ;
    sum = i+x ;
    mul=i*x ;
    sub=i-x ;
    cout << i <<" + " <<x <<" = " << sum << "\n" ;
    cout << i <<" * " <<x <<" = " << mul << "\n" ;
    cout << i <<" - " <<x <<" = " << sub << "\n" ;

    return  0 ;
}
